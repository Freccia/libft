#include "libft.h"

long		ft_pow(int nb, int pow)
{
	int		i;
	long	tot;

	i = 1;
	if (pow == 0)
		return (1);
	tot = nb;
	while (i < pow)
	{
		tot *= nb;
		++i;
	}
	return (tot);
}
