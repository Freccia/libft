char	*ft_strcat(char *dst, const char *src)
{
	int		i;
	int		j;

	i = -1;
	j = -1;
	if (!dst || !src)
		return (0);
	while (dst[++i])
		;
	while (src[++j])
	{
		dst[i++] = src[j];
	}
	dst[i] = '\0';
	return (dst);
}
