#include <libft.h>

int		ft_isascii(int c)
{
	return (c >= 0 && c <= 127);
}

int		ft_isprint(int c)
{
	return (c >= ' ' && c <= '~');
}

int		ft_isquote(int c)
{
	return ((c == '\'' || c == '\"'));
}
